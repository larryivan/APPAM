    def test_rule_files_use_template_style_layout(self):
        snakefile = Path("workflow/Snakefile").read_text(encoding="utf-8")
        convert_rules = Path("workflow/rules/convert.smk").read_text(encoding="utf-8")
        maxquant_rules = Path("workflow/rules/maxquant.smk").read_text(encoding="utf-8")
        common_rules = Path("workflow/rules/common.smk").read_text(encoding="utf-8")
        render_script = Path("workflow/scripts/render_mqpar_rule.py").read_text(encoding="utf-8")

        self.assertNotIn("from pathlib import Path", snakefile)
        self.assertNotIn('container: config.get("container_image"', snakefile)
        self.assertIn('expand("<results>/mzml/{sample}.mzML", sample=SAMPLE_IDS)', snakefile)
        self.assertIn('"<maxquant>/mqpar/all_samples.mqpar.xml"', snakefile)
        self.assertIn('"<maxquant>/combined/txt/proteinGroups.txt"', snakefile)
        self.assertNotIn("MZML_TARGETS,", snakefile)
        self.assertNotIn("str(MQPAR_OUTPUT)", snakefile)
        self.assertNotIn("str(PROTEIN_GROUPS)", snakefile)
        self.assertNotIn("RAW_MZML =", common_rules)
        self.assertNotIn("BRUKER_MZML =", common_rules)
        self.assertNotIn("FINAL_MZML =", common_rules)
        self.assertNotIn("MQPAR_OUTPUT =", common_rules)
        self.assertNotIn("PROTEIN_GROUPS =", common_rules)
        self.assertIn("rule thermo_raw_to_mzml", convert_rules)
        self.assertIn("rule bruker_d_to_mzml", convert_rules)
        self.assertIn("rule finalize_raw_mzml", convert_rules)
        self.assertIn("rule finalize_bruker_mzml", convert_rules)
        self.assertNotIn("if [ ! -f", convert_rules)
        self.assertNotIn("mkdir -p", convert_rules)
        self.assertNotIn("conda:", convert_rules)
        self.assertNotIn("container:", convert_rules)
        self.assertIn('mzml="<results>/thermo/{sample}.mzML"', convert_rules)
        self.assertIn('mzml="<results>/bruker/{sample}.mzML"', convert_rules)
        self.assertIn("outdir=lambda wildcards, output: output.mzml[:-5]", convert_rules)
        self.assertNotIn('mzml="<results>/mzml/raw/{sample}/{sample}.mzML"', convert_rules)
        self.assertNotIn('mzml=directory("<results>/mzml/bruker/{sample}")', convert_rules)
        self.assertNotIn('mzml=directory("<results>/bruker/{sample}")', convert_rules)
        self.assertIn('-b={output.mzml:q}', convert_rules)
        self.assertIn('{input.mzml:q}', convert_rules)
        self.assertIn("{input.raw:q}", convert_rules)
        self.assertIn("{output.mzml:q}", convert_rules)
        self.assertIn("mv {params.outdir:q}/*.mzML {output.mzml:q}", convert_rules)
        self.assertNotIn("if [ ! -f", maxquant_rules)
        self.assertNotIn("conda:", maxquant_rules)
        self.assertNotIn("container:", maxquant_rules)
        self.assertIn("script:", maxquant_rules)
        self.assertIn('"../scripts/render_mqpar_rule.py"', maxquant_rules)
        self.assertNotIn("run:", maxquant_rules)
        self.assertIn("> {log:q} 2>&1", maxquant_rules)
        self.assertNotIn("> ./{log:q} 2>&1", maxquant_rules)
        self.assertNotIn("from __future__ import annotations", render_script)
        self.assertNotIn("snakemake.configfiles", render_script)
        self.assertIn("config_data=snakemake.config", render_script)
        self.assertNotIn("snakemake.params.mzml_dir", render_script)
        self.assertIn("MZML_INPUTS = [Path(path) for path in snakemake.input.mzmls]", render_script)
        self.assertIn("MZML_DIR = MZML_INPUTS[0].parent", render_script)
        self.assertIn("SAMPLE_IDS", common_rules)
        self.assertIn("_raw_input", common_rules)
        self.assertIn("_d_input", common_rules)


