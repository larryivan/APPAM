    def test_template_moves_under_workflow_directory(self):
        template = Path("workflow/templates/mqpar.template.xml")
        text = template.read_text(encoding="utf-8")
        root = ET.parse(template).getroot()

        self.assertFalse(Path("templates/mqpar.template.xml").exists())
        self.assertNotIn("/data/sun_hengxi_2024/lwt/paleoproteomics", text)
        self.assertNotIn("20141110_QEp1_LC7_PhGe_4_53_W1_1.mzML", text)
        self.assertEqual(root.findtext(".//fastaFiles/FastaFileInfo/fastaFilePath"), "/path/to/reference.fasta")
        self.assertEqual(root.findtext(".//filePaths/string"), "/path/to/input.mzML")
        self.assertIsNotNone(root.find(".//parameterGroups"))
        self.assertIsNotNone(root.find(".//maxQuantVersion"))
        self.assertIsNotNone(root.find(".//lfqTopNPeptides"))
        self.assertIsNotNone(root.find(".//msmsParamsArray"))
        self.assertIsNotNone(root.find(".//matchBetweenRuns"))
        self.assertIsNotNone(root.find(".//peptideFdr"))

